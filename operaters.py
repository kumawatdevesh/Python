x = 2
y = 2

# arithmatic operators
print(x+y)
print(x-y)
print(x/y)
print(x*y)
print(x%y)

# assignment operators

l = x + 2
m = x * 3
a,b = 5, 6
print(l, m, a, b)

# negation
print(-l)

# relational operator

print(l > m)
print(l < m)
print(l==m)
print(l >= m)
print(l <= m)
print(l != m)

# logical operator
print(l > m and l < m)
print(l > m or l < m)
print(not l)

#number system conversion

print(bin(20))
print(oct(20))
print(hex(20))
